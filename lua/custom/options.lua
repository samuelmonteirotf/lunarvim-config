-- ~/.config/lvim/lua/custom/options.lua

vim.opt.relativenumber = true -- Mostrar números relativos
vim.opt.number = true -- Mostrar número da linha
vim.opt.wrap = false -- Desativar quebra de linha automática
vim.opt.shiftwidth = 2 -- Espaço usado para auto indentação
vim.opt.tabstop = 2 -- Número de espaços que uma tabulação ocupa
vim.opt.expandtab = true -- Converter tabulação em espaços
vim.opt.smartindent = true -- Indentar automaticamente código formatado corretamente

